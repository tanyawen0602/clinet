<script>
  import NavBar from './HomePage/NavBar';
  import NoticeBar from './HomePage/NoticeBar';
  export default {
    name: 'login-page',
    components: { NavBar, NoticeBar },
    data() {
      return {
        hasData: false
      };
    },
    computed: {
      notice: {
        get() {
          return this.$store.state.Home.notice
        }
      }
    },
    methods: {
      open(link) {
        this.$electron.shell.openExternal(link);
      },
      login() {
        if (global.hitbdata.table) {
          this.$store.commit('SET_NOTICE', '未注册用户登陆！');
          this.$store.commit('SET_NAVBAR', 'home');
          this.$store.commit('HAS_DATA');
          this.$router.push('/home');
        } else {
          this.hasData = true;
          this.$store.commit('SET_NOTICE', `读取系统初始化文件失败，请重新下载，放到${global.hitbdata.path.system}目录下！`)
        }
      }
    },
  };
</script>

